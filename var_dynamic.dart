void main() {
  // var

  var section;

  section = 'D';
  section = 6;
  section = false;
  // var without initial value allows reassignment with any type

  var rollno = 12;
  // it is assigned with int so rollno has became int (dynamic) type

  // rollno = 's'; // Error: rollno was assigned an int, so it cannot hold a String

  print(section);
  print(rollno);
}
